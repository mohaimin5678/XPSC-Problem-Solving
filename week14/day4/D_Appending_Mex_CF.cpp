#define no cout<<"No"<<ln
using namespace std;
void mohaimin(){
    int n;
    cin>>n;
    vector<int> v(n);
    for(int i=0;i<n;i++){
        cin>>v[i];
    }
    int serial=-1;
    for(int i=0;i<n;i++){
        if(v[i]>serial+1){
            cout<<i+1<<ln;
            return;
        }
        serial=max(serial,v[i]);
    }
    cout<<-1<<ln;
}
int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    int t;
    t=1;
    while(t--){
        mohaimin();
    }
}